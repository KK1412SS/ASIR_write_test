def letter_width_old(letter):
    strokes = LETTER_STROKES.get(letter.upper(), [])
    if not strokes:
        return 0
    return max(x for stroke in strokes for (x, y) in stroke)


def letter_width(letter):
    strokes = LETTER_STROKES.get(letter.upper(), LETTER_STROKES.get(letter, []))
    if not strokes:
        return 0

    xs = [x for stroke in strokes for (x, y) in stroke]
    return max(xs) - min(xs)